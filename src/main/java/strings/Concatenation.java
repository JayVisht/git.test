package strings;

public class Concatenation {

    public static void main(String[] args) {
        int age  = 35;
        age++;
        String firstName = "John";
        String lastName = "Lee";
        String space = " ";

        System.out.println(firstName + space + lastName);

        System.out.println(firstName + " " + lastName);

        System.out.println(firstName + age);


    }



}
